CREATE TABLE IF NOT EXISTS owners (
  id INT(4) UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  first_name VARCHAR(30),
  last_name VARCHAR(30),
  address VARCHAR(255),
  city VARCHAR(80),
  password VARCHAR(80),
  favFood VARCHAR(80),
  email VARCHAR(80),
  username VARCHAR(30),
  INDEX(last_name)
) engine=InnoDB;

CREATE TABLE IF NOT EXISTS qrcodes (
  `idQRCodes` INT NOT NULL,
  `Pointsx1000` INT NULL DEFAULT NULL,
  `Pointsx500` INT NULL DEFAULT NULL,
  `Pointsx200` INT NULL DEFAULT NULL,
  `Pointsx100` INT NULL DEFAULT NULL,
  `Pointsx50` INT NULL DEFAULT NULL,
  `Loser` INT NULL DEFAULT NULL,
  `Random` INT NULL DEFAULT NULL,
  PRIMARY KEY (`idQRCodes`));

  
